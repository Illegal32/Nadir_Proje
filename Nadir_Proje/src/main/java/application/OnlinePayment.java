    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() == cashButton) {
            CashPayment b = new CashPayment();
            dispose();
            b.setVisible(true);
        } else if (e.getSource() == payButton) {
            OnlinePayment b = new OnlinePayment();
            dispose();
            b.setVisible(true);
        } else if (e.getSource() == backButton) {
            Bill b = new Bill();
            dispose();
            b.setVisible(true);
        }
    }


